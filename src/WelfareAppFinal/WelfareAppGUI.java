                JOptionPane.showMessageDialog(null, "Contact: " + n.getContactNum() + "\n First Name: " + n.getFname() + "\n Last Name:  " + n.getLname() + "\n Prev Address: " + n.getpAddress() + "\n Current Address: " + n.getcAddress());
