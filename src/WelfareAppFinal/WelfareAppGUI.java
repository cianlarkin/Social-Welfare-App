    private void printActionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printActionBtnActionPerformed

        /*
        Submit Button for Documents with Welfare App (File Writer and and Buffered Writer used to create new Welfare.txt)
        This has also been discovered and adjusted to open and run not only in Textpad but in other .exe related files.
         */
        //If Statments to procced to enter information.
        if (fnameTb.getText().equals("") || lnameTb.getText().equals("") || pAddressTb.getText().equals("")
                || cAddressTb.getText().equals("") || contactNumTb.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "You must enter all details to print.");
        } else if (count == 0) {
            JOptionPane.showMessageDialog(null, "There is no information to print.");
        } else {

            //FILE I/O Delcare Objects
            File outFile;
            FileWriter fw;
            BufferedWriter bw;

            //Gets text from declared variables stated on the top of this page.
            fname = fnameTb.getText();
            lname = lnameTb.getText();
            pAddress = pAddressTb.getText();
            cAddress = cAddressTb.getText();
            contactNum = contactNumTb.getText();
            try {
                outFile = new File("welfare.txt");
                fw = new FileWriter(outFile, true);
                bw = new BufferedWriter(fw);

                //File Writer displays a new line of data and then the string data gets sent ahead.
                bw.newLine();
                bw.write(fname);
                bw.newLine();
                bw.write(lname);
                bw.newLine();
                bw.write(pAddress);
                bw.newLine();
                bw.write(cAddress);
                bw.newLine();
                bw.write(contactNum);
                bw.newLine();
                //Close the Buffered Stream
                bw.close();

                //Code below prints out to given .exe file which must include stated .txt, .doc, etc
                ProcessBuilder pb = new ProcessBuilder("notepad.exe", "welfare.txt");
                pb.start();
                

                //Throws in catch Exceptions to locate any undeclared variables listed above with the letter E.
            } catch (IOException e) {
                System.out.println("Error Writing to File" + e);
            }
        }
    }//GEN-LAST:event_printActionBtnActionPerformed

