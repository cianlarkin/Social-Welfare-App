    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed

        /* 
        ARRAY OF OBJECTS DELETING
        First If statement includes a condition for typing in all information and then else if gives a count.
        Code then proceeds to use the for loop that runs through each variable and then removes.
         */
        
        //If Statement included to not proceed without proper information given.
        if (fnameTb.getText().equals("") || lnameTb.getText().equals("") || pAddressTb.getText().equals("")
                || cAddressTb.getText().equals("") || contactNumTb.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "You must enter all details to delete.");

            //Else if with no information to delete
        } else if (count == 0) {
            JOptionPane.showMessageDialog(null, "There is no information to delete.");

            //Else proceed to run through Arraylist.
        } else {
            fname = fnameTb.getText();
            lname = lnameTb.getText();
            pAddress = pAddressTb.getText();
            cAddress = cAddressTb.getText();
            contactNum = contactNumTb.getText();
            
            //For each loop
            for (int i = 0; i < aList.size(); i++) {
                if (aList.get(i).getFname().equals(fname) && aList.get(i).getLname().equals(lname)
                        && aList.get(i).getpAddress().equals(pAddress) && aList.get(i).getcAddress().equals(cAddress)
                        && aList.get(i).getContactNum().equals(contactNum)) {
                    
                    //Removing of aList files on the screen.
                    aList.remove(i);
                    count = count - 1;
                    JOptionPane.showMessageDialog(null, "Deleted.");
                    ppsTb.setText("");
                    fnameTb.setText("");
                    lnameTb.setText("");
                    pAddressTb.setText("");
                    cAddressTb.setText("");
                    contactNumTb.setText("");
                    break;

                }
                if (i == count - 1) {
                    JOptionPane.showMessageDialog(null, "Details not found.");
                }
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

